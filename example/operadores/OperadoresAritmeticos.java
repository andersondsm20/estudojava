package org.example.operadores;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("=========================================");

        int adicao = 5 + 2;
        System.out.println("Resultado da adição de 5 com 2: " + adicao);

        int subtracao = 5 - 2;
        System.out.println("Resultado da subtração de 5 por 2: " + subtracao);

        int multiplicacao = 5 * 2;
        System.out.println("Resultado da multiplicação de 5 por 2: " + multiplicacao);

        double divisao = 5.0 / 2.0;
        System.out.println("resultado da divisão de 5 por 2: " + divisao);

        double moduloOuResto = 5.0 % 2.0;
        System.out.println("Resto da divisão 5 pelo 2: " + moduloOuResto);

        System.out.println("Fim!");
        System.out.println("===========================================");

    }
}
